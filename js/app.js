/* global $ */

;(function () {
  function toggleMenu () {
    $('.js-toggle-menu').toggleClass('animate')
    $('.header').toggleClass('active')
    $('.js-top-menu').toggleClass('active')
    $('body').toggleClass('menu-active')
  }

  $('.js-menu').on('click', function (e) {
    toggleMenu()
    e.preventDefault()
  })

})()
